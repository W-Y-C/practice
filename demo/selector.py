    def __init__(self,pn,word):
        self.pn=pn
        self.word=word
        self.base_url='https://image.baidu.com/search/acjson?tn=resultjson_com&logid=11293899308605915084&ipn=rj&ct=201326592&is=&fp=result&fr=&cl=2&lm=-1&ie=utf-8&oe=utf-8&adpicid=&st=&z=&ic=&hd=&latest=&copyright=&s=&se=&tab=&width=&height=&face=&istype=&qc=&nc=1&expermode=&nojc=&isAsync=&rn=30&gsm=1e0000000000001e&{}&'
        self.db=MongoDBPipeline('localhost','test_db')
        self.collection=self.db.open_spider()['images']


    def get_page(self):
        for name in self.word:
            for pt in range(1,self.pn+1):
                meta = {'pn': pt * 30, 'word': name, 'queryWord': name}
                url = (self.base_url + urlencode(meta)).format(str(int(time.time()*1000)))
                yield url


    def get_page_src(self):
        session=requests.session()
        item = WebimagesItem()
        for at in self.get_page():
            result = json.loads(session.get(at,headers=select_images.headers).text.replace("\'", "\""))
            for image in result.get("data"):
                if not image.get('thumbURL'):
                    continue
                item.url=image.get('thumbURL')
                item.title=image.get('fromPageTitle')
                item.type=image.get('type')
                item.date=image.get('bdImgnewsDate')
                item.simid=image.get('simid')
                yield item.change_type()


    # 代码优化:save_page()函数改成异步函数
    def save_page(self):
        i=1
        session=requests.session()
        for ax in self.get_page_src():
            ax['_id']=str(i)
            if key_admit:
                self.collection.save(ax)
            image_data=session.get(ax['url'],headers=select_images.headers)
            with open("./images/"+f'{i}.jpg',"wb") as f:
                f.write(image_data.content)
                print(f'正在下载第{i}张照片')
                i=i+1



    def __del__(self):
        self.db.close_spider()
        print("----关闭数据库----")
        print("亲爱的主人,已经为您下载完毕!")


    def run(self):
        self.save_page()
